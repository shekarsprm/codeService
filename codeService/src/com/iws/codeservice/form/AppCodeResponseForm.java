package com.iws.codeservice.form;

public class AppCodeResponseForm {

	private String statusCode;
	private String statusMessage;
	private String responseStatusTime;
	public String getStatusCode() {
		return statusCode;
	}
	public void setStatusCode(String statusCode) {
		this.statusCode = statusCode;
	}
	public String getStatusMessage() {
		return statusMessage;
	}
	public void setStatusMessage(String statusMessage) {
		this.statusMessage = statusMessage;
	}
	public String getResponseStatusTime() {
		return responseStatusTime;
	}

	public void setResponseStatusTime(String responseStatusTime) {
		this.responseStatusTime = responseStatusTime;
	}
	
	
	
}
